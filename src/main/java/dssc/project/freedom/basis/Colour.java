package dssc.project.freedom.basis;

/**
 * Enum that represents the colour of a {@link Stone}.
 *
 * It can be white, black or empty, thus without colour.
 * It is used to avoid working with a {@link String} all the time.
 */
public enum Colour {

    /** White {@link Stone}. */
    WHITE,
    /** Black {@link Stone}. */
    BLACK,
    /** Empty {@link Stone}. */
    NONE
}